DROP TABLE IF EXISTS Player, Deck, Card, Game CASCADE;

CREATE TABLE Player (
    PlayerId INT PRIMARY KEY AUTO_INCREMENT,
    Name NVARCHAR(50) NOT NULL
);

CREATE TABLE Deck (
    DeckId INT PRIMARY KEY AUTO_INCREMENT
);

CREATE TABLE Card (
    CardId INT PRIMARY KEY AUTO_INCREMENT,
    DeckId INT NOT NULL REFERENCES Deck (DeckId),
    DeckIndex INT NOT NULL,
    Shape TINYINT NOT NULL,
    Fill TINYINT NOT NULL,
    Color TINYINT NOT NULL,
    NrOfShapes TINYINT NOT NULL
);

CREATE TABLE Game (
    GameId INT PRIMARY KEY AUTO_INCREMENT,
    DeckId INT NOT NULL REFERENCES Deck (DeckId),
    CardIndex INT NOT NULL,
    PlayerId INT NOT NUll
);

